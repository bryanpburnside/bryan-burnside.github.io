////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(val1, val2) {
  if(val1 < val2) { // if the first value is less than the second value
    return val1; // return the first value
  } else { // otherwise
    return val2; // return the second value
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(val) {
  if(val === 0) { // if the value passed equals 0
    return true; // return true
  } else if (val === 1 || val === -1) { // if the value passed equals 1 or -1
    return false; // return false
  } else { // otherwise
   return isEven(val - 2); // call the function again, except subtract 2 from value
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let count = 0; // create a variable to keep track of our count
  for(let i = 0; i < string.length; i++) { // loop through the string argument
    if(string[i] === letter) { // if a letter in the string matches the letter argued
      count++; // increment the counter
    }
  }
  return count; // return the counter
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0; // create a variable to keep track of our count
  for(let i = 0; i < string.length; i++) { // loop through the string argument
    if(string[i] === "B") { // if the string contains a B
      count++; // increment the counter
    }
  }
  return count; // return the counter
}

